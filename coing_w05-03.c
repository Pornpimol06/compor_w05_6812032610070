#include <stdio.h>

int main() {
    // บรรทัดที่ 1: แสดงตัวแปร a พร้อมตัวเลขทศนิยมและสัญลักษณ์ %
    printf("a=%6.3f%%\n", 0.617);  // ใช้ %% เพื่อแสดงเครื่องหมาย %

    // บรรทัดที่ 2: แสดงตัวแปร b พร้อมเลขจำนวนเต็มชิดขวา
    printf("b=%4d%%\n", 25);

    // บรรทัดที่ 3: แสดงตัวแปร c พร้อมเลขเรียงกัน
    printf("c  =%6.3f%%\n", 19);

    // บรรทัดที่ 4: แสดงตัวแปร d พร้อมเลขชิดขวา
    printf("d =%-2d%%\n", 9);

    // บรรทัดที่ 5: แสดงตัวแปร e พร้อมตัวเลขเว้นวรรค
    printf("e=%7.3f%%\n", 124.555);

    // บรรทัดที่ 6: แสดงตัวแปร f พร้อมเลขทศนิยมหลายหลัก
    printf("f=%5.3f%%\n",0.001);

    return 0;
}
